use sai;


DROP TABLE ALLOCATION;
DROP TABLE PATIENTS;
DROP TABLE AREAS;
DROP TABLE DISEASES;
DROP TABLE CATEGORIES;
DROP TABLE DOCTORS;
DROP TABLE HOSPITALS;


CREATE TABLE Categories
 (
  CategoryID VARCHAR2(10) CONSTRAINT Cat_CatID_PK PRIMARY KEY,
  CategoryName VARCHAR2(25)
 );

CREATE TABLE Areas
 (
  AreaID VARCHAR2(10) CONSTRAINT Areas_AreaID_PK PRIMARY KEY,
  AreaName VARCHAR2(25)
 );

CREATE TABLE Patients
(
PatientID VARCHAR2(10) CONSTRAINT Pat_PatID_PK PRIMARY KEY,
PatientName VARCHAR2(25),
DOB DATE,
PhoneNo NUMBER(10),
AreaID VARCHAR2(10),
CONSTRAINT Area_Pat_AID_FK FOREIGN KEY(AreaID) REFERENCES Areas(AreaID)
);

CREATE TABLE Diseases
(
DiseaseID VARCHAR2(10) CONSTRAINT Dis_DisID_PK PRIMARY KEY,
DiseaseName VARCHAR2(25),
DaysToRecover NUMBER(3),
CategoryID VARCHAR2(10),
CONSTRAINT Cat_Dis_CID_FK FOREIGN KEY(CategoryID) REFERENCES Categories(CategoryID)
);

CREATE TABLE Hospitals
(
HospitalID VARCHAR2(10) CONSTRAINT Hos_HosID_PK PRIMARY KEY,
HospitalName VARCHAR2(25),
Address VARCHAR2(25),
NoOfWards NUMBER(3)
);

CREATE TABLE Doctors
(
DoctorID VARCHAR2(10) CONSTRAINT Doc_DocID_PK PRIMARY KEY,
DoctorName VARCHAR2(25),
Salary NUMBER(5),
DOB DATE,
DOJ DATE,
Dean VARCHAR2(10),
HospitalId VARCHAR2(10),
CONSTRAINT Hos_Doc_HID_FK FOREIGN KEY(HospitalId) REFERENCES Hospitals(HospitalId),
CONSTRAINT DOCT_DEAN_FK FOREIGN KEY(Dean) REFERENCES Doctors(DoctorID)
);

CREATE TABLE Allocation
(
DoctorID VARCHAR2(10),
PatientID VARCHAR2(10),
DiseaseID VARCHAR2(10),
AdmittedDate DATE,
FeesPerDay NUMBER(5),
NoOfDays NUMBER(5),
CONSTRAINT Doc_ALL_DID_FK FOREIGN KEY(DoctorID)REFERENCES Doctors(DoctorID),
CONSTRAINT Pat_ALL_PID_FK FOREIGN KEY(PatientID)REFERENCES Patients(PatientID),
CONSTRAINT Dis_ALL_DisID_FK FOREIGN KEY(DiseaseID)REFERENCES Diseases(DiseaseID),
CONSTRAINT ALL_COMP_CPK PRIMARY KEY(DoctorID,PatientID,DiseaseID,AdmittedDate)
);

